const Yup = require("yup");

exports.schema = Yup.object().shape({
    title: Yup.string()
        .required("عنوان دیدگاه الزامی می باشد")
        .min(2, "عنوان دیدگاه نباید کمتر از 2 کارکتر باشد")
        .max(100, "عنوان دیدگاه نباید بیشتر از 100 کاراکتر باشد"),


    comment: Yup.string()
        .required(" دیدگاه الزامی می باشد")
        .min(2, " دیدگاه نباید کمتر از 2 کارکتر باشد")
        .max(255, " دیدگاه نباید بیشتر از 255 کاراکتر باشد"),
  
       
  
});
